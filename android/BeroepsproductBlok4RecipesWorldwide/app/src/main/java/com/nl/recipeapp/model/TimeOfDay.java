package com.nl.recipeapp.model;

public class TimeOfDay {
    private String name;

    public TimeOfDay(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
